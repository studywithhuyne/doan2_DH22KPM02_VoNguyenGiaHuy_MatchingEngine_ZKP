#[inline]
fn err(status: StatusCode, message: &str) -> (StatusCode, Json<Value>) {
    (status, Json(json!({ "error": message })))
}

#[inline]
fn db_err(e: sqlx::Error) -> ApiError {
    err(StatusCode::INTERNAL_SERVER_ERROR, &format!("database error: {e}"))
}

#[inline]
fn conflict(message: &str) -> ApiError {
    err(StatusCode::CONFLICT, message)
}

#[inline]
fn unauthorized(message: &str) -> ApiError {
    err(StatusCode::UNAUTHORIZED, message)
}

#[inline]
fn internal(message: &str) -> ApiError {
    err(StatusCode::INTERNAL_SERVER_ERROR, message)
}

fn normalize_username(raw: &str) -> Result<String, ApiError> {
    let username = raw.trim().to_lowercase();
    if username.len() < 3 || username.len() > 32 {
        return Err(err(
            StatusCode::BAD_REQUEST,
            "username length must be between 3 and 32 characters",
        ));
    }
    if !username
        .bytes()
        .all(|b| b.is_ascii_alphanumeric() || b == b'_' || b == b'-')
    {
        return Err(err(
            StatusCode::BAD_REQUEST,
            "username may only contain [a-z0-9_-]",
        ));
    }
    Ok(username)
}

fn validate_password(password: &str) -> Result<(), ApiError> {
    if password.len() < 8 || password.len() > 128 {
        return Err(err(
            StatusCode::BAD_REQUEST,
            "password length must be between 8 and 128 characters",
        ));
    }
    Ok(())
}

fn hash_password(password: &str) -> Result<String, ApiError> {
    let salt = SaltString::generate(&mut OsRng);
    let hash = Argon2::default()
        .hash_password(password.as_bytes(), &salt)
        .map_err(|_| internal("failed to hash password"))?
        .to_string();
    Ok(hash)
}

fn verify_password(password: &str, password_hash: &str) -> Result<(), ApiError> {
    let parsed = PasswordHash::new(password_hash)
        .map_err(|_| unauthorized("invalid username or password"))?;

    Argon2::default()
        .verify_password(password.as_bytes(), &parsed)
        .map_err(|_| unauthorized("invalid username or password"))
}

fn auth_response(user_id: u64, username: String) -> AuthResponse {
    AuthResponse {
        user_id: user_id.to_string(),
        username,
        auth_mode: "x-user-id".to_string(),
        auth_header: format!("x-user-id: {user_id}"),
    }
}
