type ApiError = (StatusCode, Json<Value>);

const MAX_JS_SAFE_INTEGER_I64: i64 = 9_007_199_254_740_991;

/// POST /api/auth/register
///
/// Creates a new user with Argon2id-hashed password and initializes zero
/// balances for all assets found in `assets`.
pub async fn register_handler(
    State(state): State<AppState>,
    Json(body): Json<RegisterRequest>,
) -> Result<(StatusCode, Json<AuthResponse>), ApiError> {
    let username = normalize_username(&body.username)?;
    validate_password(&body.password)?;
    let password_hash = hash_password(&body.password)?;

    let mut tx = state.db.begin().await.map_err(db_err)?;

    // Serialize user ID allocation in dev/test mode to avoid race conditions
    // with BIGINT IDs generated by application logic.
    sqlx::query("SELECT pg_advisory_xact_lock($1)")
        .bind(92_011_i64)
        .execute(&mut *tx)
        .await
        .map_err(db_err)?;

    let (next_id,): (i64,) = sqlx::query_as(
        "SELECT COALESCE(MAX(id), 0) + 1
         FROM users
         WHERE id > 0 AND id <= $1",
    )
        .bind(MAX_JS_SAFE_INTEGER_I64)
        .fetch_one(&mut *tx)
        .await
        .map_err(db_err)?;

    if next_id <= 0 || next_id > MAX_JS_SAFE_INTEGER_I64 {
        return Err(internal("generated invalid user id"));
    }

    let inserted = sqlx::query(
        "INSERT INTO users (id, username, password_hash)
         VALUES ($1, $2, $3)
         ON CONFLICT (username) DO NOTHING",
    )
    .bind(next_id)
    .bind(&username)
    .bind(&password_hash)
    .execute(&mut *tx)
    .await
    .map_err(db_err)?;

    if inserted.rows_affected() == 0 {
        return Err(conflict("username already exists"));
    }

    sqlx::query(
        "INSERT INTO balances (user_id, asset_symbol, available, locked)
         SELECT $1, symbol, 0, 0 FROM assets
         ON CONFLICT (user_id, asset_symbol) DO NOTHING",
    )
    .bind(next_id)
    .execute(&mut *tx)
    .await
    .map_err(db_err)?;

    tx.commit().await.map_err(db_err)?;

    let user_id = u64::try_from(next_id).map_err(|_| internal("user id overflow"))?;
    Ok((
        StatusCode::CREATED,
        Json(auth_response(user_id, username)),
    ))
}

/// POST /api/auth/login
///
/// Verifies username/password with Argon2id and returns user identity metadata.
/// Clients must send `x-user-id` in subsequent authenticated requests.
pub async fn login_handler(
    State(state): State<AppState>,
    Json(body): Json<LoginRequest>,
) -> Result<Json<AuthResponse>, ApiError> {
    let username = normalize_username(&body.username)?;
    validate_password(&body.password)?;

    let row: Option<(i64, String, Option<String>)> = sqlx::query_as(
        "SELECT id, username, password_hash
         FROM users
         WHERE username = $1",
    )
    .bind(&username)
    .fetch_optional(&state.db)
    .await
    .map_err(db_err)?;

    let (id, db_username, password_hash) = row.ok_or_else(|| unauthorized("invalid username or password"))?;
    let stored_hash = password_hash.ok_or_else(|| unauthorized("invalid username or password"))?;

    verify_password(&body.password, &stored_hash)?;

    let user_id = u64::try_from(id).map_err(|_| internal("invalid user id in database"))?;
    Ok(Json(auth_response(user_id, db_username)))
}

/// GET /api/auth/me
///
/// Uses `x-user-id` extraction and returns the associated username.
pub async fn me_handler(
    State(state): State<AppState>,
    UserId(user_id): UserId,
) -> Result<Json<AuthResponse>, ApiError> {
    let row: Option<(String,)> = sqlx::query_as("SELECT username FROM users WHERE id = $1")
        .bind(user_id as i64)
        .fetch_optional(&state.db)
        .await
        .map_err(db_err)?;

    let (username,) = row.ok_or_else(|| unauthorized("invalid x-user-id"))?;
    Ok(Json(auth_response(user_id, username)))
}

/// GET /api/auth/users
///
/// Returns all users in the database for admin/testing selectors.
pub async fn users_handler(
    State(state): State<AppState>,
    UserId(_caller_id): UserId,
) -> Result<Json<Vec<UserListItem>>, ApiError> {
    let rows: Vec<(i64, String)> = sqlx::query_as(
        "SELECT id, username
         FROM users
         WHERE id > 0
         ORDER BY id ASC",
    )
    .fetch_all(&state.db)
    .await
    .map_err(db_err)?;

    let users = rows
        .into_iter()
        .filter_map(|(id, username)| {
            u64::try_from(id).ok().map(|uid| UserListItem {
                user_id: uid.to_string(),
                username,
            })
        })
        .collect();

    Ok(Json(users))
}

